import math

a = min(10, 20, 30, 40, 50)  # minimum
print(a)

b = max(50, 100, 70, 80, 90, 10)  # maximum
print(b)

k = abs(-1500)  # absolute value
print(k)

# power of variable
# x: A number, the base
# y: A number, the exponent
# z: Optional. A number, the modulus

x = pow(4, 2)  # Return the value of 4 to the power of 2
print(x)

u = pow(4, 2, 7)  # Return the value of 4 to the power of 2, modulus 7
print(u)
